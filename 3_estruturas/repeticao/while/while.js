let x = 10;

console.log("Contagem regressiva:");

while (x > 0) {
  console.log(x);
  x = x - 1;
}
console.log("Fim da contagem.");
console.log("---------------------");

x = 0;

console.log("Contagem de 1 a 10:");

while (x <= 10) {
  console.log(x);
  x = x + 1;
}

console.log("Fim da contagem.");

